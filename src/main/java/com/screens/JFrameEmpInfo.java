            jTextFieldPhoneNo.setText(eDetailsJTable.getPhoneNumber());
            jTextFieldSSS.setText(eDetailsJTable.getSSS());
            jTextFieldPhilHealth.setText(eDetailsJTable.getPhilHealth());
            jTextFieldTIN.setText(eDetailsJTable.getTIN());
            jTextFieldPAGIBIG.setText(eDetailsJTable.getPagIbig());
            jTextFieldPosition.setText(eDetailsJTable.getPosition());
            jTextFieldSupervisor.setText(eDetailsJTable.getImmediateSupervisor());
