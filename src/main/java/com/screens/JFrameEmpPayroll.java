        jButtonPayroll1.setForeground(new Color(0, 0, 0));
        
        this.id = empID;
        this.selectedMonth = selectedMonth;
        this.currentYear = currentYear;

        // Load Work Hours for the employee ID within a selected month of the year
        
        loadEmpDetails(empID);
        jLabelCurrentYear.setText(String.valueOf(currentYear));
        jComboMonth.setSelectedIndex(selectedMonth);
        
        // Get the basic salary and hourly rate
        this.totalAllowances = calcEmpAllowances(empID);
        
        // Get the calculated hours (or minutes) worked for the chosen month
        // Added 1 to the SelectedIndex because arrays start with 0.
        loadWorkHours(empID, jComboMonth.getSelectedIndex()+1, currentYear);

        // Get the Gross Salary
        this.grossSalary = calcGrossSalary(basicSalary, hourlyRate, MinutesWorked, OvertimeMinutes, UndertimeMinutes);
        
        // Get the Deductions
        this.totalDeductions = calcEmpDeductions(empID, this.grossSalary);
        
        // Get the Net Salary
        this.netSalary = calcNetSalary(this.grossSalary, this.totalAllowances, this.totalDeductions);
        
