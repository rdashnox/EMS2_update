    private void jComboMonthItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboMonthItemStateChanged

        // Get the basic salary and hourly rate
        this.totalAllowances = calcEmpAllowances(this.id);
        
        // Get the calculated hours (or minutes) worked for the chosen month
        // Added 1 to the SelectedIndex because arrays start with 0.
        loadWorkHours(this.id, jComboMonth.getSelectedIndex()+1, currentYear);

        // Get the Gross Salary
        this.grossSalary = calcGrossSalary(basicSalary, hourlyRate, MinutesWorked, OvertimeMinutes, UndertimeMinutes);
        
        // Get the Deductions
        this.totalDeductions = calcEmpDeductions(this.id, this.grossSalary);
        
        // Get the Net Salary
        this.netSalary = calcNetSalary(this.grossSalary, this.totalAllowances, this.totalDeductions);                
    }//GEN-LAST:event_jComboMonthItemStateChanged

