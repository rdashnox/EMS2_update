    private void loadWorkHours(int empID, int selectedMonth, int currentYear){
        EmpAttendance ea = new EmpAttendance();
        List<AttendanceTrack> listAttendance = ea.getTimesheetByIDandDate(empID, selectedMonth, currentYear);
        ea.calculateTotalHoursWorked(listAttendance, selectedMonth, currentYear);
        
        //Save the calc hours in different variables for the gross salary calculation
        MinutesWorked = ea.calcMinutesWorked;
        OvertimeMinutes = ea.calcOvertimeMinutes;
        UndertimeMinutes = ea.calcUndertimeMinutes;

        jTextFieldHoursWorked.setText(Long.toString(MinutesWorked/60) + ":" + Long.toString(MinutesWorked%60) + ":00");
        jTextFieldOvertimeHours.setText(Long.toString(OvertimeMinutes/60) + ":" + Long.toString(OvertimeMinutes%60) + ":00");
        jTextFieldUndertimeHours.setText(Long.toString(UndertimeMinutes/60) + ":" + Long.toString(UndertimeMinutes%60) + ":00");
    }

private float calcNetSalary(float gsalary, float totalAllowance, float totalDeduction) {
    float netsal = gsalary + totalAllowance - totalDeduction;
    jTextFieldNetSalary.setText(String.format("%.2f",netsal));
    return netsal;
}    
    
    
    
private float calcGrossSalary(float basicSalary, long hrRate, long minuteWork, long overtimeWork, long undertimeWork){
        float monthlySalary = (float) minuteWork * hrRate;
        float overtimePay = (float) 0.00;
        float undertimeDeduction = (float) 0.00;
        float gs = (float) 0.00;
        
     // Calculated monthly salary using regular minutes worked cannot exceed the basic MOnthly salary. Excess should be charged to overtime Pay.
    if (monthlySalary >= basicSalary){
	monthlySalary = basicSalary;
    }
    
    if (overtimeWork > 0) {
        // This is the Overtime formula based on PH Taxation
        overtimePay = ((OvertimeMinutes / 60) * hourlyRate) / (WorkHoursCalculator.REGULAR_WORK_HOURS);

    } else {
        overtimePay = 0;
    }    

    if (undertimeWork > 0) {
        // This is the Overtime formula based on PH Taxation
        undertimeDeduction = ((OvertimeMinutes / 60) * hourlyRate) / (WorkHoursCalculator.REGULAR_WORK_HOURS);

    } else {
        undertimeDeduction = 0;
    }      

    gs = monthlySalary + overtimePay - undertimeDeduction;
    // Bind the values to the jTextFields
    jTextFieldBasicSalary.setText(String.format("%.2f",monthlySalary));
    jTextFieldHourlyRate.setText(String.format("%.2f",(float) hrRate));
    jTextFieldGrossSalary.setText(String.format("%.2f",gs));

    return gs;
}
    
    private float calcEmpDeductions(int empID, float gsal){
        float td = (float) 0.00;
        try{
        if (empID != 0) {
            BenefitsCalculator calc = new BenefitsCalculator(empID);  
            
            EmpDeductions empDed = calc.CalculateDeductions(gsal);
            // Bind the Deductions to the text fields
            jTextFieldSSSContrib.setText(String.format("%.2f",empDed.getSSS()));
            jTextFieldPhilHealthContrib.setText(String.format("%.2f",empDed.getPhilHealth()));
            jTextFieldPagIBIGContrib.setText(String.format("%.2f",empDed.getPagIBIG()));
            //these are already considered once the total hours are calculated because there will be less hours worked if the employee went on VL or got late.
            // jTextFieldTardiness
            // jLabelAbsences
            jTextFieldTax.setText(String.format("%.2f",empDed.getWithholdingTax()));
            jTextFieldTotalDeduction.setText(String.format("%.2f",empDed.getTotalDeductions()));
            td = empDed.getTotalDeductions();
        }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, "Error in retrieving info: " + e.getMessage());
        }
        
        return td;
    }    
    
